    private void ingresarTxtMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ingresarTxtMouseClicked
        if (!validarLetras(nombreTxt.getText().trim())) {
            JOptionPane.showMessageDialog(rootPane, "El nombre no es correcto(Solo puede ingresar letras)");
            if (!validarLetras(apellidoTxt.getText().trim())) {
                JOptionPane.showMessageDialog(rootPane, "El apellido no es correcto(Solo puede ingresar letras)");
                if (!emailTxt.getText().contains("@") && !emailTxt.getText().contains(".")) {
                    JOptionPane.showMessageDialog(rootPane, "El correo ingresado es inv√°lido");
                }
            }
        } else if (!validarLetras(apellidoTxt.getText().trim())) {
            JOptionPane.showMessageDialog(rootPane, "El apellido no es correcto(Solo puede ingresar letras)");
            if (!emailTxt.getText().contains("@") && !emailTxt.getText().contains(".")) {
                JOptionPane.showMessageDialog(rootPane, "El correo ingresado es inv√°lido");
            }
        } else if (!emailTxt.getText().contains("@") && !emailTxt.getText().contains(".")) {
            JOptionPane.showMessageDialog(rootPane, "El correo ingresado es inv√°lido");
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Bienvenido " + nombreTxt.getText(), "CaracteristicasFisicas", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            CaracteristicasFisicas a = new CaracteristicasFisicas();
            a.setVisible(true);
            this.setVisible(false);
        }
    }//GEN-LAST:event_ingresarTxtMouseClicked

    public static boolean validarLetras(String datos){
        return datos.matches("[a-zA-Z]*");
    }
    
